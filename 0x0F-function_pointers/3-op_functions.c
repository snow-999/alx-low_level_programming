#include "3-calc.h"
/**
 * op_add- add two numbers
 * @a: num
 * @b: num
 * Return: (a + b)
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub numbers
 * @a: num
 * @b: num
 * Return: (a - b)
 *
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul two numbers
 * @a: num
 * @b: number
 * Return: (a * b)
 *
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division two numbers
 * @a: num
 * @b: num
 * Return: (a / b)
 *
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - remainder of two numbers
 * @a: num
 * @b: num
 * Return: (a % b)
 *
*/
int op_mod(int a, int b)
{
	return (a % b);
}
